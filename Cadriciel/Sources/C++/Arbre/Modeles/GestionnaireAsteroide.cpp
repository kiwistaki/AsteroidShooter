void GestionnaireAsteroide::genererAsteroides(int numAsteroides,
											  int numCellX, int numCellY, int numCellZ,
											  float rayonSphere,
											  float ellipseXMin, float ellipseXMax,
											  float ellipseYMin, float ellipseYMax,
											  float ellipseZMin, float ellipseZMax,
											  float variationSinMin, float variationSinMax,
											  float variationTexelMin, float variationTexelMax)
{
	rayonSphere_ = rayonSphere;
	ellipseXMin_ = ellipseXMin;
	ellipseXMax_ = ellipseXMax;
	ellipseYMin_ = ellipseYMin;
	ellipseYMax_ = ellipseYMax;
	ellipseZMin_ = ellipseZMin;
	ellipseZMax_ = ellipseZMax;
	variationSinMin_ = variationSinMin;
	variationSinMax_ = variationSinMax;
	variationTexelMin_ = variationTexelMin;
	variationTexelMax_ = variationTexelMax;

	int numVertexX = numCellX + 1;
	int numVertexY = numCellY + 1;
	int numVertexZ = numCellZ + 1;

	float dx = 1.0f / numCellX;
	float dy = 1.0f / numCellY;
	float dz = 1.0f / numCellZ;

	// On active la génération automatique des normales pour les faces et
	// le résultat sera correct pour cette application
	glEnable(GL_AUTO_NORMAL);
	glEnable(GL_NORMALIZE);

	for (int n = 0; n < numAsteroides; n++) {
		// Création de la texture 3D. Elle va être utilisée dans la fonction de densité
		vector<vector<vector<float> > > texture3D = textureWorleyNoise3D(numVertexX, numVertexY, numVertexZ);

		// Création des points et des valeurs de la surface pour tous les sommets des
		// cubes subdivisés
		vector<vector<vector<aiVector3D> > > points;
		vector<vector<vector<float> > > values;

		for (int i = 0; i < numVertexX; i++) {
			points.push_back(vector<vector<aiVector3D> >());
			values.push_back(vector<vector<float> >());

			for (int j = 0; j < numVertexY; j++) {
				points[i].push_back(vector<aiVector3D>());
				values[i].push_back(vector<float>());

				for (int k = 0; k < numVertexZ; k++) {
					// On centre les points
					float x = (i * dx - 0.5f) * 2.0f;
					float y = (j * dy - 0.5f) * 2.0f;
					float z = (k * dz - 0.5f) * 2.0f;
					aiVector3D point(x, y, z);

					float densite = fonctionDensite(point, texture3D[i][j][k]);

					points[i][j].push_back(point);
					values[i][j].push_back(densite);
				}
			}
		}

		// Maintenant on génère les triangles
		vector<aiVector3D> vertices;

		for (int i = 0; i < numCellX; i++) {
			for (int j = 0; j < numCellY; j++) {
				for (int k = 0; k < numCellZ; k++) {
					GridCell_t cell = {
						points[i    ][j    ][k    ],
						points[i + 1][j    ][k    ], 
						points[i + 1][j + 1][k    ], 
						points[i    ][j + 1][k    ], 
						points[i    ][j    ][k + 1], 
						points[i + 1][j    ][k + 1], 
						points[i + 1][j + 1][k + 1], 
						points[i    ][j + 1][k + 1],

						values[i    ][j    ][k    ],
						values[i + 1][j    ][k    ], 
						values[i + 1][j + 1][k    ], 
						values[i    ][j + 1][k    ], 
						values[i    ][j    ][k + 1], 
						values[i + 1][j    ][k + 1], 
						values[i + 1][j + 1][k + 1], 
						values[i    ][j + 1][k + 1]
					};

					// Algorithme de Marcihng Cube
					polygonise(cell, vertices);
				}
			}
		}

		// On map la texture3D sur une 8x8x8 puisqu'on n'ira jamais sur les
		// sommets des bords
		// Couleur brunâtre-rouge pour la couleur caillou
		float r = 0x5f;
		float g = 0x4e;
		float b = 0x43;

		vector<unsigned char> texels;
		for (int i = 0; i < numCellX; i++) {
			for (int j = 0; j < numCellY; j++) {
				for (int k = 0; k < numCellZ; k++) {
					// On éclaircie un peu la texture car elle est très sombre
					float pixel = 1.0f - texture3D[i][j][k] * 2.0f;
					float pixelR = pixel * r + r*0.75f;
					float pixelG = pixel * g + g*0.75f;
					float pixelB = pixel * b + b*0.75f;

					texels.push_back(((unsigned char)pixelR));
					texels.push_back(((unsigned char)pixelG));
					texels.push_back(((unsigned char)pixelB));
				}
			}
		}

		// Création de la texture
		GLuint texId;
		glGenTextures(1, &texId);
		glBindTexture(GL_TEXTURE_3D, texId);
		glTexParameteri(GL_TEXTURE_3D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
		glTexParameteri(GL_TEXTURE_3D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
		glTexParameteri(GL_TEXTURE_3D, GL_TEXTURE_WRAP_S, GL_REPEAT);
		glTexParameteri(GL_TEXTURE_3D, GL_TEXTURE_WRAP_T, GL_REPEAT);
		glTexParameteri(GL_TEXTURE_3D, GL_TEXTURE_WRAP_R, GL_REPEAT);
		glTexImage3D(GL_TEXTURE_3D, 0, GL_RGB8, numCellX, numCellY, numCellZ, 0, GL_RGB, GL_UNSIGNED_BYTE, &texels[0]);
		
		// On créer la callisse
		GLuint liste = glGenLists(1);
		glNewList(liste, GL_COMPILE);
			glBegin(GL_TRIANGLES);
			for (int i = (int) vertices.size() - 1; i >= 0; i--) {
					float x = vertices[i].x;
					float y = vertices[i].y;
					float z = vertices[i].z;

					float s = x / 2.0f + 0.5f;
					float t = y / 2.0f + 0.5f;
					float r = z / 2.0f + 0.5f;

					float length = sqrtf(x*x + y*y + z*z); //* 2.0f;

					glNormal3f(x / length, y / length, z / length);
					glTexCoord3f(s, t, r);
					glVertex3f(x, y, z);
				}
			glEnd();
		glEndList();

		listeAsteroides_.push_back(liste);

		// Calcul du rayon : on trouve les points extrêmes et on calcule le rayon par la suite
		Vecteur3f xMin, xMax, yMin, yMax, zMin, zMax;
		for (size_t i = 0; i < vertices.size(); i++) {
			float x = vertices[i].x;
			float y = vertices[i].y;
			float z = vertices[i].z;

			if (x<xMin[0]) { xMin[0]=x; xMin[1]=y; xMin[2]=z; } else if (x>xMax[0]) { xMax[0]=x; xMax[1]=y; xMax[2]=z; }
			if (y<yMin[1]) { yMin[0]=x; yMin[1]=y; yMin[2]=z; } else if (y>yMax[1]) { yMax[0]=x; yMax[1]=y; yMax[2]=z; }
			if (z<zMin[2]) { zMin[0]=x; zMin[1]=y; zMin[2]=z; } else if (z>zMax[2]) { zMax[0]=x; zMax[1]=y; zMax[2]=z; }
		}

		double max = 0.0;
		double norme = 0.0;
		norme = xMin.norme2(); if (norme > max) max = norme;
		norme = yMin.norme2(); if (norme > max) max = norme;
		norme = zMin.norme2(); if (norme > max) max = norme;

		norme = xMax.norme2(); if (norme > max) max = norme;
		norme = yMax.norme2(); if (norme > max) max = norme;
		norme = zMax.norme2(); if (norme > max) max = norme;

		rayonAsteroides_.push_back(sqrtf((float)max));
	}
}

void GestionnaireAsteroide::detruireAsteroides() {
	for (size_t i = 0; i < listeAsteroides_.size(); i++) {
		glDeleteLists(listeAsteroides_[i], 1);
	}

	listeAsteroides_.clear();
	rayonAsteroides_.clear();
}

void GestionnaireAsteroide::getModeleAsteroide(unsigned int& liste, float& rayon) const {
	int idx = rand() % listeAsteroides_.size();
	liste = listeAsteroides_[idx];
	rayon = rayonAsteroides_[idx];
}

unsigned int GestionnaireAsteroide::poissonLookUp(unsigned int value) const {
	if (value < 393325350) return 1;
	if (value < 1022645910) return 2;
	if (value < 1861739990) return 3;
	if (value < 2700834071) return 4;
	if (value < 3372109335) return 5;
	if (value < 3819626178) return 6;
	if (value < 4075350088) return 7;
	if (value < 4203212043) return 8;
	return 9;
}

unsigned int GestionnaireAsteroide::lcgRandom(unsigned int lastValue) const {
	return (unsigned int)((1103515245u * lastValue + 12345u) % 0x100000000u);
}

unsigned int GestionnaireAsteroide::hashValues(unsigned int i, unsigned int j, unsigned int k) const {
	return (unsigned int)((((((OFFSET_BASIS ^ i) * FNV_PRIME) ^ j) * FNV_PRIME) ^ k) * FNV_PRIME);
}

void GestionnaireAsteroide::insererTableauDistances(vector<float>& distArray, float value) const {
	float tmp;

	// Si la valeur que l'on veut ajouter au tableau est plus grande que celle qui est la plus grande,
	// alors on ne l'ajoute pas.
	for (int i = ((int)distArray.size() - 1); i >= 0; i--) {
		if (value > distArray[i]) {
			break;
		}

		tmp = distArray[i];
		distArray[i] = value;
		if (i + 1 < distArray.size()) {
			distArray[i + 1] = tmp;
		}
	}
}

float GestionnaireAsteroide::genererCouleurWorley(const aiVector3D& input, int seed, vector<float>& distArray) const {
	unsigned int lastRandom, numberFeaturePoints;
	aiVector3D randomDiff;
	aiVector3D featurePoint;
	int cubeX, cubeY, cubeZ;

	for (size_t i = 0; i < distArray.capacity(); i++) {
		distArray.push_back(6666.0f);
	}

	int evalCubeX = (int)floor(input[0]);
	int evalCubeY = (int)floor(input[1]);
	int evalCubeZ = (int)floor(input[2]);

	for (int i = -1; i < 2; i++) {
		for (int j = -1; j < 2; j++) {
			for (int k = -1; k < 2; k++) {
				cubeX = evalCubeX + i;
				cubeY = evalCubeY + j;
				cubeZ = evalCubeZ + k;

				lastRandom = lcgRandom(hashValues((unsigned int)(cubeX + seed), (unsigned int)(cubeY), (unsigned int)(cubeZ)));
				numberFeaturePoints = poissonLookUp(lastRandom);

				for (unsigned int l = 0; l < numberFeaturePoints; l++) {
					lastRandom = lcgRandom(lastRandom);
					randomDiff.x = (float)lastRandom / 0x100000000;

					lastRandom = lcgRandom(lastRandom);
					randomDiff.y = (float)lastRandom / 0x100000000;

					lastRandom = lcgRandom(lastRandom);
					randomDiff.z = (float)lastRandom / 0x100000000;

					featurePoint.x = randomDiff.x + (float)cubeX;
					featurePoint.y = randomDiff.y + (float)cubeY;
					featurePoint.z = randomDiff.z + (float)cubeZ;

					// On utilise la distance euclédienne au carré
					aiVector3D diff = input - featurePoint;
					insererTableauDistances(distArray, diff * diff);
				}
			}
		}
	}

	float color = distArray[1] - distArray[0];
	color = min(1.0f, color);
	color = max(0.0f, color);

	return color;
}

vector<vector<vector<float> > > GestionnaireAsteroide::textureWorleyNoise3D(int width, int height, int depth) const {
	float zoom = 5.0f;

	vector<vector<vector<float> > > texture3D;

	for (int x = 0; x < width; x++) {
		texture3D.push_back(vector<vector<float> >());

		for (int y = 0; y < height; y++) {
			texture3D[x].push_back(vector<float>());

			for (int z = 0; z < depth; z++) {
				int seed = rand() % 10000;

				aiVector3D input((x / (float)width) * zoom, y / ((float)height) * zoom, z / ((float)depth) * zoom);
				vector<float> distArray;
				distArray.reserve(3);

				float color = genererCouleurWorley(input, seed, distArray);
				texture3D[x][y].push_back(color);
			}
		}
	}

	return texture3D;
}

void GestionnaireAsteroide::polygonise(const GridCell_t& cell, vector<aiVector3D>& vertices) const {
	int cubeIndex = 0, numTriangles = 0;
	aiVector3D vertexList[12];

	if (cell.val[0] < 0.0f) cubeIndex |=  1;
	if (cell.val[1] < 0.0f) cubeIndex |=  2;
	if (cell.val[2] < 0.0f) cubeIndex |=  4;
	if (cell.val[3] < 0.0f) cubeIndex |=  8;
	if (cell.val[4] < 0.0f) cubeIndex |= 16;
	if (cell.val[5] < 0.0f) cubeIndex |= 32;
	if (cell.val[6] < 0.0f) cubeIndex |= 64;
	if (cell.val[7] < 0.0f) cubeIndex |= 128;

	// Si le cube est complètement en dehors de la surface
	if (edgeTable[cubeIndex] == 0) {
		return;
	}
	
	// Trouvons les sommets où la surface intersecte le cube
	if (edgeTable[cubeIndex] & 1) {
		aiVector3D vertex = sommetInterpoler(cell.pos[0], cell.pos[1], cell.val[0], cell.val[1]);
		vertexList[0] = vertex;
	}

	if (edgeTable[cubeIndex] & 2) {
		aiVector3D vertex = sommetInterpoler(cell.pos[1], cell.pos[2], cell.val[1], cell.val[2]);
		vertexList[1] = vertex;
	}

	if (edgeTable[cubeIndex] & 4) {
		aiVector3D vertex = sommetInterpoler(cell.pos[2], cell.pos[3], cell.val[2], cell.val[3]);
		vertexList[2] = vertex;
	}

	if (edgeTable[cubeIndex] & 8) {
		aiVector3D vertex = sommetInterpoler(cell.pos[3], cell.pos[0], cell.val[3], cell.val[0]);
		vertexList[3] = vertex;
	}

	if (edgeTable[cubeIndex] & 16) {
		aiVector3D vertex = sommetInterpoler(cell.pos[4], cell.pos[5], cell.val[4], cell.val[5]);
		vertexList[4] = vertex;
	}

	if (edgeTable[cubeIndex] & 32) {
		aiVector3D vertex = sommetInterpoler(cell.pos[5], cell.pos[6], cell.val[5], cell.val[6]);
		vertexList[5] = vertex;
	}

	if (edgeTable[cubeIndex] & 64) {
		aiVector3D vertex = sommetInterpoler(cell.pos[6], cell.pos[7], cell.val[6], cell.val[7]);
		vertexList[6] = vertex;
	}

	if (edgeTable[cubeIndex] & 128) {
		aiVector3D vertex = sommetInterpoler(cell.pos[7], cell.pos[4], cell.val[7], cell.val[4]);
		vertexList[7] = vertex;
	}

	if (edgeTable[cubeIndex] & 256) {
		aiVector3D vertex = sommetInterpoler(cell.pos[0], cell.pos[4], cell.val[0], cell.val[4]);
		vertexList[8] = vertex;
	}

	if (edgeTable[cubeIndex] & 512) {
		aiVector3D vertex = sommetInterpoler(cell.pos[1], cell.pos[5], cell.val[1], cell.val[5]);
		vertexList[9] = vertex;
	}

	if (edgeTable[cubeIndex] & 1024) {
		aiVector3D vertex = sommetInterpoler(cell.pos[2], cell.pos[6], cell.val[2], cell.val[6]);
		vertexList[10] = vertex;
	}

	if (edgeTable[cubeIndex] & 2048) {
		aiVector3D vertex = sommetInterpoler(cell.pos[3], cell.pos[7], cell.val[3], cell.val[7]);
		vertexList[11] = vertex;
	}

	// Création des triangles
	for (int i = 0; triTable[cubeIndex][i] != -1; i+= 3) {
		aiVector3D vert1 = vertexList[triTable[cubeIndex][i    ]];
		aiVector3D vert2 = vertexList[triTable[cubeIndex][i + 1]];
		aiVector3D vert3 = vertexList[triTable[cubeIndex][i + 2]];

		vertices.push_back(vert1);
		vertices.push_back(vert2);
		vertices.push_back(vert3);
	}
}

aiVector3D GestionnaireAsteroide::sommetInterpoler(const aiVector3D& p1, const aiVector3D& p2,
												   float v1, float v2) const
{
	float t = -v1 / (v2 - v1);
	return p1 + t * (p2 - p1);
}

float GestionnaireAsteroide::fonctionDensite(const aiVector3D& point, float texel) const {
	float x = point.x;
	float y = point.y;
	float z = point.z;

	float ellipseX = aleatoireDansInterval(ellipseXMin_, ellipseXMax_);
	float ellipseY = aleatoireDansInterval(ellipseYMin_, ellipseYMax_);
	float ellipseZ = aleatoireDansInterval(ellipseZMin_, ellipseZMax_);
	float varSin = aleatoireDansInterval(variationSinMin_, variationSinMax_);
	float varTexel = aleatoireDansInterval(variationTexelMin_, variationTexelMax_);

	return (x*x*ellipseX + y*y*ellipseY + z*z*ellipseZ + sinf(texel)*varSin + texel * varTexel) - rayonSphere_;
}

float GestionnaireAsteroide::aleatoireDansInterval(float min, float max) const {
	float random = ((float) rand()) / (float) RAND_MAX;
	float diff = max - min;
	float r = random * diff;
	return min + r;
}

